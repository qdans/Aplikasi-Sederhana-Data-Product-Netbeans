 public void LoadProductNo(){
     
     try{
     pst = con.prepareStatement("SELECT id FROM product_tbl");
     rs = pst.executeQuery();
     txtpid.removeAllItems();
     while(rs.next()){
         txtpid.addItem(rs.getString(1));
     }
     }catch (SQLException ex){
       Logger.getLogger(productForm.class.getName()).log(Level.SEVERE, null, ex);
     }
 }

 private void Fetch(){
        try {
            int q;
            pst = con.prepareStatement("SELECT * FROM product_tbl");
            rs = pst.executeQuery();
            ResultSetMetaData rss = rs.getMetaData();
            q = rss.getColumnCount();
            
            DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            while(rs.next()){
                Vector v2 = new Vector();
                for(int a=1; a<=1; a++){
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("pnama"));
                    v2.add(rs.getString("harga"));
                    v2.add(rs.getString("jumlah"));
                }
                df.addRow(v2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(productForm.class.getName()).log(Level.SEVERE, null, ex);
        }
 }
 
