    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            // TODO add your handling code here:
            
            String pnama = txtPnama.getText();
            String harga = txtHarga.getText();
            String jumlah = txtJumlah.getText();
            
            pst = con.prepareStatement("INSERT INTO product_tbl (pnama,harga,jumlah)VALUES(?,?,?)");
            pst.setString(1, pnama);
            pst.setString(2, harga);
            pst.setString(3, jumlah);
            
            int k = pst.executeUpdate();
            
            if(k==1){
                JOptionPane.showMessageDialog(this,"Record Added! successfully!");
                txtPnama.setText("");
                txtHarga.setText("");
                txtJumlah.setText("");
                Fetch();
                LoadProductNo();
            }else{
               JOptionPane.showMessageDialog(this,"Record failed to saved!!"); 
            }
         
        } catch (SQLException ex) {
            Logger.getLogger(productForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try {
            // TODO add your handling code here:
            
            String pid = txtpid.getSelectedItem().toString();
            
            pst = con.prepareStatement("SELECT * FROM product_tbl WHERE id=?");
            pst.setString(1, pid);
            rs=pst.executeQuery();
            
            if(rs.next()==true){
                txtPnama.setText(rs.getString(2));
                txtHarga.setText(rs.getString(3));
                txtJumlah.setText(rs.getString(4));
                
            }else{
                JOptionPane.showMessageDialog(this,"No record found!!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(productForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            // TODO add your handling code here:
            
            String pnama = txtPnama.getText();
            String harga = txtHarga.getText();
            String jumlah = txtJumlah.getText();
            String pid = txtpid.getSelectedItem().toString();
            
            pst = con.prepareStatement("UPDATE product_tbl SET pnama=?,harga=?,jumlah=? WHERE id=?");
            
            pst.setString(1,pnama);
            pst.setString(2,harga);
            pst.setString(3, jumlah);
            pst.setString(4,pid);
            
            int k=pst.executeUpdate();
            if(k==1){
                JOptionPane.showMessageDialog(this,"Record has been successfully update!!");
                txtPnama.setText("");
                txtHarga.setText("");
                txtJumlah.setText("");
                txtPnama.requestFocus();
                Fetch();
                LoadProductNo();
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(productForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

